char --almacenar tipo de datos de ancho fijo.
varchar --almacena tipo de datos alfanumericos de ancho variable
text --almacena tipos de datos texto.
nchar --almacena tipo de datos de ancho fijo. Corta los espacios sobrantes.
nvarchar --almacena tipo de datos de ancho variable. Corta los espacios sobrantes.
bit --almacena valores de 1 y 0
int --almacena enteros
bigint --enteros con limites muy grandes
decimal --almacena valores entre -10^38 + 1 y 10^38 -1
numeric -- misma funcion que decimal, pero nos permite valores enteros y decimales
money -- mismos limites que bigint. Se usa para finanzas.
float --punto y coma.
